package Model;

/**
 *
 * @author aluno
 */
public class Funcionario extends Pessoa{
    
    public Funcionario (int idade, String nome, String cpf){
        super (idade, nome, cpf);
    }

    @Override
    public int getIdade() {
        return idade;
    }

    @Override
    public void setIdade(int idade) {
        this.idade = idade;
    }

    @Override
    public String getNome() {
        return nome;
    }

    @Override
    public void setNome(String nome) {
        this.nome = nome;
    }

    @Override
    public String getCpf() {
        return cpf;
    }

    @Override
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }


}
